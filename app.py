    def get_categories(self) -> List[str]:
        """Получить список категорий"""
        return list(set(term["category"] for term in self.terms))
    
    def filter_terms(self, category: str = "Все", search_query: str = "") -> List[Dict]:
        """Фильтрация терминов"""
        filtered = self.terms
        
        if category != "Все":
            filtered = [term for term in filtered if term["category"] == category]
        
        if search_query:
            query = search_query.lower()
            filtered = [
                term for term in filtered
                if (query in term["term"].lower() or
                    query in term["translation"].lower() or
                    query in term["definition"].lower())
            ]
        
        return filtered

